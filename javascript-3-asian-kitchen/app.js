var category = ["All", "Korea", "Japan", "China"]

const btnContainer = document.querySelector(".btn-container");
const section = document.querySelector(".section-center");

makeCategoryBtn = () => {
  var catButtons = category.map( item => {
    return `<button class="btn btn-outline-primary m-1">${item}</button>`
  })

  btnContainer.innerHTML = catButtons.join("")

  const filterButtons = document.querySelectorAll(".btn");
  console.log(filterButtons)
  filterButtons.forEach((btn) => {
    btn.addEventListener("click", (e) => {
      const category = e.currentTarget.innerHTML;
      const menuCategory = menu.filter((menuItem) => {
        if (menuItem.category === category) {
          return menuItem;
        }
      });
      if (category === "All") {
        renderMenu(menu);
      } else {
        renderMenu(menuCategory);
      }
    });
  });
};

renderMenu = (cat) => {
  
  var innerSection = cat.map(item => {
    return `
    <div class="col-md-2 mb-4">
      <img class="rounded-3" src=${item.img} alt=${item.desc} style="width: 12vw; height:22vh">
    </div>
    <div class="col-md-4 mb-4">
      <div class="d-flex justify-content-between ">
        <h4>${item.title}</h4>
        <h4>${item.price} $</h4>
      </div>
      <hr>
      <p>${item.desc}</p>
    </div>
    `
  })
  
  section.innerHTML = innerSection.join("")
  
}

makeCategoryBtn()
renderMenu(menu)